		HAL_RTC_GetDate(&hrtc, &sDate, RTC_FORMAT_BIN);
		sprintf(strDate, "%d %s 20%d", sDate.Date, months[sDate.Month - 1], sDate.Year);
		sprintf(strTime, "%02d:%02d:%02d", sTime.Hours, sTime.Minutes, sTime.Seconds);
